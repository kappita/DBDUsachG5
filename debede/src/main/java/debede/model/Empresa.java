package debede.model;

/**
 *
 * @author joaquin
 */
public class Empresa {
    private int id;
    private String nombre;

    public Empresa(String nombre, int id) {
        this.id = id;
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
}
